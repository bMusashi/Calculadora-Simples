namespace Calculadora
{
    internal class Calculator
    {
        internal static double Sum(double firstValue, double secondValue)
        {
            return firstValue + secondValue;
        }
        internal static double Sub(double firstValue, double secondValue)
        {
            return firstValue - secondValue;
        }
        internal static double Mul(double firstValue, double secondValue)
        {
            return firstValue * secondValue;
        }
        internal static double Div(double firstValue, double secondValue)
        {
            return firstValue / secondValue;
        }
    }
}